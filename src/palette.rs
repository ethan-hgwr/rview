const LUT_SIZE: usize = 1024;

const fn build_brightness_lut() -> [u8; LUT_SIZE] {
    let mut lut = [0u8; LUT_SIZE];
    let mut i = 0;
    while i < LUT_SIZE {
        let b = i as f32 / (LUT_SIZE - 1) as f32;
        let mut index = BRIGHTNESS_LEVELS.len() - 1;
        let mut j = 0;
        while j < BRIGHTNESS_LEVELS.len() {
            if b <= BRIGHTNESS_LEVELS[j] {
                index = j;
                break;
            }
            j += 1;
        }
        lut[i] = index as u8;
        i += 1;
    }
    lut
}

static BRIGHTNESS_LUT: [u8; LUT_SIZE] = build_brightness_lut();

pub(crate) fn map_brightness_to_char(b: f32) -> char {
    let i = (b * (LUT_SIZE - 1) as f32) as usize;
    let index = BRIGHTNESS_LUT[i];
    *PALETTE.get(index as usize).unwrap_or(&'▓')
}
